package model;

public class Stock_view {
	private String br_type;
	private int tot;
	private int ntot;
	private String s_kind;
	private int b_code;
	private String st_code;
	private String usable;
	public String getBr_type() {
		return br_type;
	}
	public void setBr_type(String br_type) {
		this.br_type = br_type;
	}
	public int getTot() {
		return tot;
	}
	public void setTot(int tot) {
		this.tot = tot;
	}
	public int getNtot() {
		return ntot;
	}
	public void setNtot(int ntot) {
		this.ntot = ntot;
	}
	public String getS_kind() {
		return s_kind;
	}
	public void setS_kind(String s_kind) {
		this.s_kind = s_kind;
	}
	public int getB_code() {
		return b_code;
	}
	public void setB_code(int b_code) {
		this.b_code = b_code;
	}
	public String getSt_code() {
		return st_code;
	}
	public void setSt_code(String st_code) {
		this.st_code = st_code;
	}
	public String getUsable() {
		return usable;
	}
	public void setUsable(String usable) {
		this.usable = usable;
	}
	
	
}
